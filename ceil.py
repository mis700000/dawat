'''ceil(x) Description The method ceil()
returns ceiling value of x - the smallest integer not less than x.
Syntax Following is the syntax for ceil() method'''


import math

print (math.ceil(-5.1))
print ( math.ceil(5))
print (math.ceil(100.12))
print (math.ceil(100.73))



